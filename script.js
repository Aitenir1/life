function view(){
	var str = '';
	for(i=1;i<31;i++){
		for(j=1;j<31;j++){
			if(game[i][j] == 0){
				str+="<div id="+i.toString()+"-"+j.toString()+" class=\"cage\"></div>";
			}
			else if(game[i][j] == 1){
				str+="<div id="+i.toString()+"-"+j.toString()+" class=\"cage black\"></div>";	
			}
		}
	}
	$("#game").html(str);
}

